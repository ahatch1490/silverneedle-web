namespace SilverNeedle.Characters
{
    /// <summary>
    /// Ability score types.
    /// </summary>
    public enum AbilityScoreTypes
    {
        /// <summary>
        /// Represents the strength of a character
        /// </summary>
        Strength,

        /// <summary>
        /// Represents the dexterity or agility of a character
        /// </summary>
        Dexterity,

        /// <summary>
        /// Represents the endurance or constitution of a character
        /// </summary>
        Constitution,

        /// <summary>
        /// Represents the brains of a character
        /// </summary>
        Intelligence,

        /// <summary>
        /// Represents the knowledge of a character
        /// </summary>
        Wisdom,

        /// <summary>
        /// Represents the charm of a character
        /// </summary>
        Charisma
    }
}